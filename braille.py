# Define a mapping from alphabet letters to Braille Unicode characters
BRAILLE_MAP = {
    'a': '⠁', 'b': '⠃', 'c': '⠉', 'd': '⠙', 'e': '⠑',
    'f': '⠋', 'g': '⠛', 'h': '⠓', 'i': '⠊', 'j': '⠚',
    'k': '⠅', 'l': '⠇', 'm': '⠍', 'n': '⠝', 'o': '⠕',
    'p': '⠏', 'q': '⠟', 'r': '⠗', 's': '⠎', 't': '⠞',
    'u': '⠥', 'v': '⠧', 'w': '⠺', 'x': '⠭', 'y': '⠽', 'z': '⠵',
    ' ': ' ',
    '+': '⠖', '-': '⠤', '=': '⠶', '*': '⠔', '/': '⠌',
    '(': '⠷', ')': '⠾', '[': '⠪', ']': '⠻', '{': '⠸', '}': '⠹',
    '<': '⠣', '>': '⠜', '@': '⠈', '&': '⠯', '%': '⠹',
    '^': '⠘', '~': '⠸', '#': '⠼', '$': '⠫', '€': '⠫', '£': '⠫',
    '°': '⠘', '©': '⠉', '®': '⠗', '™': '⠞', 'μ': '⠥', 'π': '⠏',
}

def text_to_braille(text):
    return ''.join(BRAILLE_MAP.get(char, '?') for char in text.lower())
